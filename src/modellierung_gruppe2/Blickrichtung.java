package modellierung_gruppe2;

/**
 * Enum das die Blickrichtungen der Figur beinhaltet.
 *
 * @author Gruppe 2
 *
 */
public enum Blickrichtung {
    /**
     * Blickrichtung oben.
     */
    OBEN,
    /**
     * Blickrichtung unten.
     */
    UNTEN,
    /**
     * Blickrichtung links.
     */
    LINKS,
    /**
     * Blickrichtung rechts.
     */
    RECHTS
}